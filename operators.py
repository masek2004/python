#arithmetic operator
num1=45
num2=50
print(num1+num2)
print(num1-num2)
print(num1*num2)
print(num1/num2)
print(num1%num2)
#assignment operator
x=5
y=10
z=10
print(x==y) #equals
print(x>y<z) #greater
print(x>=y) #greater or equals to
print(x<y>z) #less than
print(x<=y) #less
print(x != y) #not equal to
#logical operator
print(x>y and x==5)
print(x>y or x==5)
print(x<y and x==z)
print(x<y or x==z)
